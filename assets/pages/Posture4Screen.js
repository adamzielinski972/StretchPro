import React, { useState } from 'react';
import { View, Text, Image, StyleSheet, SafeAreaView, Dimensions, TouchableOpacity, ScrollView, Modal } from 'react-native';

const screenWidth = Dimensions.get('window').width;
const screenHeight = Dimensions.get('window').height;

const RPW = (percentage) => {
  return (percentage / 100) * screenWidth;
}

const RPH = (percentage) => {
  return (percentage / 100) * screenHeight;
}

export default function Posture4Screen({ navigation }) {
  const [modalVisible, setModalVisible] = useState(false);
  const [currentPopup, setCurrentPopup] = useState({ image: null, title: '', instructions: '' , offsetX: 0, offsetY: 0, colourBG: '#ffffff'});

  const goBack = async () => {
    try {
      navigation.navigate('PostureOptions');
    } catch (error) {
      console.log('Error navigating: ', error);
    }
  }

  const Stretch = ({ image, title, length, onPress }) => {
    return (
      <TouchableOpacity onPress={onPress}>
        <View style={styles.stretch}>
          <View style={styles.stretchImageContainer}>
            <Image style={styles.stretchImage} source={image} />
          </View>
          <View style={styles.stretchTexts}>
            <Text style={styles.stretchTitle}>{title}</Text>
            <Text style={styles.stretchLength}>{length}</Text>
          </View>
        </View>
      </TouchableOpacity>
    );
  };

  const PopUp = ({ image, title, instructions }) => {
    return (
      <Modal
        animationType="slide"
        transparent={true}
        visible={modalVisible}
        onRequestClose={() => setModalVisible(false)}
      >
          <View style={styles.popUp}>
            <View style={styles.popUpTitleContainer}>
              <Text style={styles.popUpTitle}>{title}</Text>
              <TouchableOpacity style={styles.exitPopUp} onPress={() => setModalVisible(false)}>
                <Image style={styles.exitPopUpImage} source={require('./images/close.png')}/>
              </TouchableOpacity>
            </View>
            <ScrollView style={{paddingBottom: RPH(10)}}>
              <View  style={{alignItems: 'center'}}>
                <View style={styles.popUpImageContainer}>
                  <Image style={styles.popUpImage} source={image} />
                </View>
              </View>
              <View style={styles.popUpTextContainer}>
                <Text style={{color: 'grey', fontSize: 14, fontWeight: 'bold'}}>INSTRUCTIONS</Text>
                <Text style={{marginTop: RPH(1), color: 'black', lineHeight: 20}}>{instructions}</Text>
              </View>
            </ScrollView>
          </View>
      </Modal>
    );
  };

  const handlePress = (image, title, instructions , offsetX, offsetY, colourBG) => {
    setCurrentPopup({ image, title, instructions , offsetX, offsetY, colourBG});
    setModalVisible(true);
  };

  return (
    <SafeAreaView style={styles.safeArea}>
      {modalVisible && <View style={styles.overlay} />}
      <View style={styles.header}>
        <View style={styles.exitButtonContainer}>
          <TouchableOpacity style={styles.exitButton} onPress={goBack}>
            <Image style={styles.exitImage} source={require('./images/close.png')}/>
          </TouchableOpacity>
        </View>
        <View style={styles.titleContainer}>
          <Text style={styles.titleText}>Posture 4</Text>
        </View>
      </View>
      <ScrollView contentContainerStyle={styles.scrollViewContent}>
        <PopUp image={currentPopup.image} title={currentPopup.title} instructions={currentPopup.instructions} offsetX={currentPopup.offsetX} offsetY={currentPopup.offsetY} colourBG={currentPopup.colourBG} />
        <Stretch image={require('./images/Squat-Hold.png')} title="Squat Hold" length={"0:30"} onPress={() => handlePress(require('./images/Squat-Hold.png'), "Squat Hold", "Start standing up with your feet together. Keep your back straight and heels on the ground and bend your knees until your thighs are nearly parallel with the floor. Hold the position.")}/>
        <Stretch image={require('./images/Split-Lunge-Hold.png')} title="Split Lunge Hold" length={"1:00"} onPress={() => handlePress(require('./images/Split-Lunge-Hold.png'), "Split Lunge Hold", "Start standing with your feet hip-width apart. Step forward with your right leg, lowering your body until both knees are bent at around a 90-degree angle. Keep your right knee above your ankle and your left knee slightly off the floor. Relax your shoulders and keep a straight spine and torso and hold the position.")}/>
        <Stretch image={require('./images/Side-Lunge-Hold.png')} title="Side Lunge Hold" length={"1:00"} onPress={() => handlePress(require('./images/Side-Lunge-Hold.png'), "Side Lunge Hold", "Start standing with a wide stance and a slight bend in your knees. Keeping your back and torso upright, shift your body weight to your right leg, bending your knee until your right thigh is parallel with the floor. Keep your left leg straight and your arms extended out in front of you or on your hips and hold the position.")}/>
        <Stretch image={require('./images/Wall-Sit.png')} title="Wall Sit" length={"0:30"} onPress={() => handlePress(require('./images/Wall-Sit.png'), "Wall Sit", "Start standing against a wall with your feet shoulder-width apart and your arms at your sides. Walk your feet about 2 feet forward and slide your body down the wall until your thighs are parallel to the floor and your knees are positioned directly above your ankles. Keep your head and upper body rested flat against the wall and hold the position.")}/>
      </ScrollView>
      <TouchableOpacity style={styles.startButton} onPress={() => navigation.navigate('Posture4Stretch')}>
        <View style={styles.startButtonTextContainer}>
          <Text style={styles.startButtonText}>START</Text>
        </View>
      </TouchableOpacity>
    </SafeAreaView>
  );
}

